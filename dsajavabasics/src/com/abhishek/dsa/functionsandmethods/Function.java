package com.abhishek.dsa.functionsandmethods;

public class Function {
    public static void main(String[] args) {
        // Function is a block of code which performs a specific task and can be reused
        // multiple times in a program. It helps in breaking down a large program into
        // smaller, manageable pieces, making it easier to read and maintain.

        //Method is a function that is associated with an object. It is a block of code that performs a specific task and can be called on an object. Methods are defined within a class and can access the data members of the class.

        // Function syntax:
        // returnType functionName(parameters) {
        // // function body
        // }

        // Call a function
        printHelloWorld();

    }

    // Print Hello World
    public static void printHelloWorld() {
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
    }

}
